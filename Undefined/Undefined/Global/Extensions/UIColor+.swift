extension UIColor {

  @nonobjc class var veryLightPink: UIColor {
    return UIColor(white: 234.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var veryLightPinkTwo: UIColor {
    return UIColor(white: 242.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var lightishPurple: UIColor {
    return UIColor(red: 162.0 / 255.0, green: 62.0 / 255.0, blue: 1.0, alpha: 1.0)
  }

  @nonobjc class var paleLavender: UIColor {
    return UIColor(red: 239.0 / 255.0, green: 227.0 / 255.0, blue: 1.0, alpha: 1.0)
  }

}
